class Solution:
    def maxA(self, N):
        """
        :type N: int
        :rtype: int
        """
        dp = [0] * (N + 1)
        dps = [0] * (N + 3)
        for i in range(1, N + 1):
            dp[i] = max(dp[i], dp[i - 1] + 1)
            for j in range(max(1, i - 7), i - 1):
                dp[i] = max(dp[i], dps[j] * (i - j + 1))
            dps[i + 2] = dp[i]
        return dp[N]


